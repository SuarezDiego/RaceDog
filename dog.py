class Dog:
    """
    Attributes:
        name: str
            Name of the dog
        date_of_birth: date
            Date of the birth of the dog
        breed: str
            Breed of the dog
    """
    def __init__(self, name, date_of_birth, breed):
        self.name = name
        self.date_of_birth = date_of_birth
        self.breed = breed